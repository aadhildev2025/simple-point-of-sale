    private void loadTableData() {
    Connection conn = null;
    PreparedStatement pst = null;
    ResultSet rs = null;

    try {
        conn = MyConn.getConnection();
        String query = "SELECT barcode, name, category, price, stock FROM products";
        pst = conn.prepareStatement(query);
        rs = pst.executeQuery();

        DefaultTableModel model = (DefaultTableModel) tblpurchase.getModel();
        model.setRowCount(0); // Clear table before loading new data

        while (rs.next()) {
            model.addRow(new Object[]{
                rs.getString("barcode"),
                rs.getString("name"),
                rs.getString("category"),
                rs.getDouble("price"),
                rs.getInt("stock")
            });
        }

    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this, "Error Loading Data: " + e.getMessage(), "Database Error", JOptionPane.ERROR_MESSAGE);
        e.printStackTrace();
    } finally {
        try {
            if (rs != null) rs.close();
            if (pst != null) pst.close();
            if (conn != null) conn.close();
        } catch (SQLException ex) {
            ex.printStackTrace();
        }
    }
    }
    

