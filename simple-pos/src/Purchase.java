    private void btnaddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnaddActionPerformed
        // TODO add your handling code here:
    
    String barcode = txtbarcode.getText().trim();
    String product = txtproduct.getText().trim();
    String category = comoct.getSelectedItem().toString().trim(); // Get selected category from combobox
    String priceText = txtprice.getText().trim();
    String qtyText = txtqty.getText().trim();

    // Validate input
    if (barcode.isEmpty() || product.isEmpty() || category.isEmpty() || priceText.isEmpty() || qtyText.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Please fill all fields!", "", JOptionPane.WARNING_MESSAGE);
        return;
    }

    double price;
    int stock;

    try {
        price = Double.parseDouble(priceText);
        stock = Integer.parseInt(qtyText);
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Invalid price or quantity value!", "", JOptionPane.ERROR_MESSAGE);
        return;
    }

    Connection conn = null;
    PreparedStatement pst = null;

    try {
        conn = MyConn.getConnection();

        // Check if product already exists
        String checkQuery = "SELECT stock FROM products WHERE barcode = ?";
        pst = conn.prepareStatement(checkQuery);
        pst.setString(1, barcode);
        ResultSet rs = pst.executeQuery();

        if (rs.next()) {
            // If product exists, update stock quantity
            int existingQty = rs.getInt("stock");
            int newQty = existingQty + stock;

            String updateQuery = "UPDATE products SET qty = ? WHERE barcode = ?";
            pst = conn.prepareStatement(updateQuery);
            pst.setInt(1, newQty);
            pst.setString(2, barcode);
            pst.executeUpdate();
        } else {
            // If product does not exist, insert a new record
            String insertQuery = "INSERT INTO products (barcode, name, category, price, stock) VALUES (?, ?, ?, ?, ?)";
            pst = conn.prepareStatement(insertQuery);
            pst.setString(1, barcode);
            pst.setString(2, product);
            pst.setString(3, category);
            pst.setDouble(4, price);
            pst.setInt(5, stock);
            pst.executeUpdate();
        }

        JOptionPane.showMessageDialog(this, "Product Added to Purchase!", "", JOptionPane.INFORMATION_MESSAGE);

        // Refresh the table to update stock
        loadTableData();

        // Clear input fields after adding
        txtbarcode.setText("");
        txtproduct.setText("");
        txtprice.setText("");
        txtqty.setText("");
        comoct.setSelectedIndex(0); // Reset combobox selection

    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this, "Database Error: " + e.getMessage(), "", JOptionPane.ERROR_MESSAGE);
        e.printStackTrace();
    } finally {
        try {
            if (pst != null) pst.close();
            if (conn != null) conn.close();
        } catch (SQLException ex) {
            ex.printStackTrace();
        }
    }
        
    }//GEN-LAST:event_btnaddActionPerformed

    private void btnsearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsearchActionPerformed
        // TODO add your handling code here:
    
    String searchTerm = txtbarcode.getText().trim();

    if (searchTerm.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Please enter a product name or barcode to search", "", JOptionPane.WARNING_MESSAGE);
        return;
    }

    Connection conn = null;
    PreparedStatement pst = null;
    ResultSet rs = null;

    try {
        conn = MyConn.getConnection();
        String query = "SELECT name, category, price, stock, barcode FROM products WHERE name LIKE ? OR barcode LIKE ?";
        
        pst = conn.prepareStatement(query);
        String searchPattern = "%" + searchTerm + "%";
        pst.setString(1, searchPattern);
        pst.setString(2, searchPattern);

        rs = pst.executeQuery();
        DefaultTableModel model = (DefaultTableModel) tblpurchase.getModel();
        model.setRowCount(0);

        if (!rs.isBeforeFirst()) { 
            JOptionPane.showMessageDialog(this, "No products found!", "", JOptionPane.INFORMATION_MESSAGE);
        } else {
            while (rs.next()) {
                model.addRow(new Object[]{
                    rs.getString("barcode"),
                    rs.getString("name"),
                    rs.getDouble("price"),
                    rs.getInt("stock"),
                    rs.getString("category")
                });
            }
        }
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this, "Database Error: " + e.getMessage(), "", JOptionPane.ERROR_MESSAGE);
        e.printStackTrace(); // Print stack trace for debugging
    } finally {
        try {
            if (rs != null) rs.close();
            if (pst != null) pst.close();
            if (conn != null) conn.close();
        } catch (SQLException ex) {
            ex.printStackTrace();
        }
    }
        
    }//GEN-LAST:event_btnsearchActionPerformed

    private void btndeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btndeleteActionPerformed
        // TODO add your handling code here:
    
    int selectedRow = tblpurchase.getSelectedRow();

    if (selectedRow == -1) {
        JOptionPane.showMessageDialog(this, "Please select a product to delete!", " ", JOptionPane.WARNING_MESSAGE);
        return;
    }

    // Get barcode from the selected row (assuming barcode is in column 0)
    String barcode = tblpurchase.getValueAt(selectedRow, 0).toString();

    int confirm = JOptionPane.showConfirmDialog(this, "Are you sure you want to delete this product?", "Confirm Deletion", JOptionPane.YES_NO_OPTION);
    if (confirm != JOptionPane.YES_OPTION) {
        return; // Exit if user cancels deletion
    }

    Connection conn = null;
    PreparedStatement pst = null;

    try {
        conn = MyConn.getConnection();
        String query = "DELETE FROM products WHERE barcode = ?";
        pst = conn.prepareStatement(query);
        pst.setString(1, barcode);

        int deleted = pst.executeUpdate();
        if (deleted > 0) {
            JOptionPane.showMessageDialog(this, "Product deleted successfully!", "", JOptionPane.INFORMATION_MESSAGE);
            loadTableData(); // Refresh table after deletion
            
            txtbarcode.setText("");
            
            
        } else {
            JOptionPane.showMessageDialog(this, "Error: Product not found!", "", JOptionPane.ERROR_MESSAGE);
        }

    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this, "Database Error: " + e.getMessage(), "", JOptionPane.ERROR_MESSAGE);
        e.printStackTrace();
    } finally {
        try {
            if (pst != null) pst.close();
            if (conn != null) conn.close();
        } catch (SQLException ex) {
            ex.printStackTrace();
        }
    }   
        
     
    }//GEN-LAST:event_btndeleteActionPerformed

